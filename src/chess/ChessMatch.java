package chess;

import java.util.ArrayList;
import java.util.List;
import java.util.stream.Collectors;

import boardgame.Board;
import boardgame.Piece;
import boardgame.Position;
import chess.pieces.Bishop;
import chess.pieces.King;
import chess.pieces.Knight;
import chess.pieces.Pawn;
import chess.pieces.Queen;
import chess.pieces.Rook;

public class ChessMatch {
	
	private int turn;
	private Color currentPlayer;
	private Board board;
	private boolean check;
	private boolean checkMate;
	
	private List<Piece> piecesOnTheBoard = new ArrayList<>();
	private List<Piece> capturedPieces = new ArrayList<>();
		
	public ChessMatch() {
		board = new Board(8, 8);							// define o tamanho do tabuleiro
		turn = 1;
		currentPlayer = Color.WHITE;
		// não é necessário setar 'check' para 'false', pois esta variável já inicia neste valor por padrão
		initialSetup();
	}
	
	public int getTurn() {
		return turn;
	}
	
	public Color getCurrentPlayer () {
		return currentPlayer;
	}
	
	public boolean getCheck() {
		return check;
	}
	
	public boolean getCheckMate() {
		return checkMate;
	}
	
	public ChessPiece[][] getpieces() {
		ChessPiece[][] mat = new ChessPiece[board.getRows()][board.getColumns()];
		for (int i=0; i<board.getRows(); i++) {
			for (int j=0; j< board.getColumns(); j++) {
				mat[i][j] = (ChessPiece) board.piece(i, j);
			}
		}
		return mat;
	}
	
	public boolean[][] possibleMoves(ChessPosition sourcePosition) {	// permite imprimir os movimentos possíveis da peça, a partir de uma posição de origem
		Position position = sourcePosition.toPosition();
		validateSourcePosition(position);
		return board.piece(position).possibleMoves();
	}
	
	public ChessPiece preformChessMove(ChessPosition sourcePosition, ChessPosition targetPosition) {
		Position source = sourcePosition.toPosition();
		Position target = targetPosition.toPosition();
		validateSourcePosition(source);						// responsável pela validação da posição de origem
		validateTargetPosition(source, target);				// responsável pela validação da posição de destino
		Piece capturedPiece = makeMove(source, target);		// 'makeMove' será responsável por realizar o movimento da peça
		
		if (testCheck(currentPlayer)) {						// testa se o jogador se colocou em xeque
			undoMove(source, target, capturedPiece);		// desfaz o movimento
			throw new ChessException("Voce nao pode se colocar em xeque");
		}
		
		check = (testCheck(opponent(currentPlayer))) ? true : false; // testa se o oponente está em xeque
		
		if (TestCheckMate(opponent(currentPlayer))) {
			checkMate = true;
		}
		else {
			nextTurn();											// para trocar o turno do jogador
		}
		
		return (ChessPiece)capturedPiece; 
	}
	
	private Piece makeMove(Position source, Position target) {
		ChessPiece p = (ChessPiece)board.removePiece(source);	// retira a peça do tabuleiro
		p.increaseMoveCount();
		Piece capturedPiece = board.removePiece(target);		// tira do tabuleiro uma possível peça capturada, localizada na posição de destino
		board.placePiece(p, target);							// coloca, na posição de destino, a peça que estava na posição de origem
		
		if (capturedPiece != null) {
			piecesOnTheBoard.remove(capturedPiece);				// retira a peça da lista de peças no tabuleiro
			capturedPieces.add(capturedPiece);					// adiciona na lista de peças capturadas
		}
		
		return capturedPiece;
	}
	
	private void undoMove(Position source, Position target, Piece capturedPiece) {
		ChessPiece p = (ChessPiece)board.removePiece(target);	// retira a peça que chegou na posição de destino
		p.decreaseMoveCount();
		board.placePiece(p, source);							// recoloca a peça na posição de origem
		
		if (capturedPiece != null) {							// teste se houve captura de peça
			board.placePiece(capturedPiece, target);			// devolve a peça capturada a sua posição
			capturedPieces.remove(capturedPiece);				// retira a peça capturada da lista de peças que foram capturadas
			piecesOnTheBoard.add(capturedPiece);				// adiciona a peça na lista de peças que estão no tabuleiro 
		}
	}
	
	private void validateSourcePosition(Position position) {
		if (!board.thereIsAPiece(position)) {
			throw new ChessException("Nao existe peca na posição de origem");
		}
		
		if (currentPlayer != ((ChessPiece)board.piece(position)).getColor()) {
			throw new ChessException("A peca escolhida nao e sua");
		}
		if (!board.piece(position).isThereAnyPossibleMove()) {
			throw new ChessException("Nao existe movimentos possiveis para a peca escolhida");
		}
	}
	
	private void validateTargetPosition(Position source, Position target) {
		if (!board.piece(source).possibleMove(target)) {		// testa se a posição de destino NÃO é um movimento possível para a peça de origem	
			throw new ChessException("A peca escolhida nao pode se mover para a posicao de destino"); 
		}
	}
	
	private void nextTurn() {
		turn++;
		currentPlayer = (currentPlayer == Color.WHITE) ? Color.BLACK : Color.WHITE;
	}
	
	private Color opponent(Color color) {
		return (color == Color.WHITE) ? Color.BLACK : Color.WHITE; 
	}
	
	private ChessPiece king(Color color) {
		List<Piece> list = piecesOnTheBoard.stream().filter(x -> ((ChessPiece)x).getColor() == color).collect(Collectors.toList());
		for (Piece p : list) {
			if (p instanceof King) {
				return (ChessPiece)p;
			}
		}
		throw new IllegalStateException("O rei " + color + "nao esta no tabuleiro");
	}
	
	private boolean testCheck(Color color) {
		Position kingPosition = king(color).getChessPosition().toPosition(); // para descobrir a posição do rei
		List<Piece> opponentPieces = piecesOnTheBoard.stream().filter(x -> ((ChessPiece)x).getColor() == opponent(color)).collect(Collectors.toList()); // descobre a lista de peças do oponente
		for (Piece p : opponentPieces) {									// vai testar todas as peças do oponente
			boolean[][] mat = p.possibleMoves();								// vai descobrir todos os possíveis movimentos da peça 'p'
			if (mat[kingPosition.getRow()][kingPosition.getColumn()]) {		// se a posição na variável 'mat' coincidir com a posição do rei...
				return true;												// quer dizer que o rei está em cheque, e retorna 'true'
			}
		}
		return false;														// significa que o rei não está em xeque
	}
	
	private boolean TestCheckMate(Color color) {
		if (!testCheck(color)) {												// verifica se este jogador está em xeque
			return false;
		}
		List<Piece> list = piecesOnTheBoard.stream().filter(x -> ((ChessPiece)x).getColor() == color).collect(Collectors.toList()); // pegar todas as peças do jogador
		for (Piece p : list) {											// para descobrir se alguma peça do jogador consegue evitar o mate
			boolean[][] mat = p.possibleMoves();
			for (int i=0; i<board.getRows(); i++) {						// percorre as linhas da matriz
				for (int j=0; j<board.getColumns(); j++) {				// percorre as colunas da matriz
					if (mat[i][j]) {
						Position source = ((ChessPiece)p).getChessPosition().toPosition();
						Position target = new Position(i, j);
						Piece capturedPiece = makeMove(source, target);
						boolean testCheck = testCheck(color);
						undoMove(source, target, capturedPiece);
						if (!testCheck) {
							return false;
						}
					}
				}
			}
		}
		return true;
		
	}
	
	private void placeNewPiece(char column, int row, ChessPiece piece) {
		board.placePiece(piece, new ChessPosition(column, row).toPosition());	// coloca a peça no tabuleiro
		piecesOnTheBoard.add(piece);											// coloca a peça na lista de peças do tabuleiro
	}
	
	private void initialSetup() {								// responsável por iniciar a partida, colocando as peças no tabuleiro
		placeNewPiece('a', 1, new Rook(board, Color.WHITE));
		placeNewPiece('b', 1, new Knight(board, Color.WHITE));
		placeNewPiece('c', 1, new Bishop(board, Color.WHITE));
		placeNewPiece('d', 1, new Queen(board, Color.WHITE));
        placeNewPiece('e', 1, new King(board, Color.WHITE));
        placeNewPiece('f', 1, new Bishop(board, Color.WHITE));
        placeNewPiece('g', 1, new Knight(board, Color.WHITE));
        placeNewPiece('h', 1, new Rook(board, Color.WHITE));
        placeNewPiece('a', 2, new Pawn(board, Color.WHITE));
        placeNewPiece('b', 2, new Pawn(board, Color.WHITE));
        placeNewPiece('c', 2, new Pawn(board, Color.WHITE));
        placeNewPiece('d', 2, new Pawn(board, Color.WHITE));
        placeNewPiece('e', 2, new Pawn(board, Color.WHITE));
        placeNewPiece('f', 2, new Pawn(board, Color.WHITE));
        placeNewPiece('g', 2, new Pawn(board, Color.WHITE));
        placeNewPiece('h', 2, new Pawn(board, Color.WHITE));
        
        placeNewPiece('a', 8, new Rook(board, Color.BLACK));
        placeNewPiece('b', 8, new Knight(board, Color.BLACK));
        placeNewPiece('c', 8, new Bishop(board, Color.BLACK));
        placeNewPiece('d', 8, new Queen(board, Color.BLACK));
        placeNewPiece('e', 8, new King(board, Color.BLACK));
        placeNewPiece('f', 8, new Bishop(board, Color.BLACK));
        placeNewPiece('g', 8, new Knight(board, Color.BLACK));
        placeNewPiece('h', 8, new Rook(board, Color.BLACK));
        placeNewPiece('a', 7, new Pawn(board, Color.BLACK));
        placeNewPiece('b', 7, new Pawn(board, Color.BLACK));
        placeNewPiece('c', 7, new Pawn(board, Color.BLACK));
        placeNewPiece('d', 7, new Pawn(board, Color.BLACK));
        placeNewPiece('e', 7, new Pawn(board, Color.BLACK));
        placeNewPiece('f', 7, new Pawn(board, Color.BLACK));
        placeNewPiece('g', 7, new Pawn(board, Color.BLACK));
        placeNewPiece('h', 7, new Pawn(board, Color.BLACK));
        }
}
